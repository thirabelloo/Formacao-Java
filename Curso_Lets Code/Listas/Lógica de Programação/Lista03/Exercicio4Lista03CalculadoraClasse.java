package Listas.Lista03;

public class Exercicio4Lista03CalculadoraClasse {

    public static float OperacaoSoma(float num1, float num2) {
        return (num1 + num2);
    }

    public static float OperacaoSubtrair(float num1, float num2) {
        return (num1 - num2);
    }

    public static float OperacaoMultiplicao(float num1, float num2) {
        return (num1 * num2);
    }

    public static float OperacaoDivisao(float num1, float num2) {
        return (num1 / num2);
    }

}
