package CursoAdaF1rst.Aula04.Aula;

/**
 * Utilizando if e else if
 */
public class CondicionaisControleFluxoParte2 {

    public static void main(String[] args) {

        System.out.println("Inicio da Aplicação");
        int valor = 10;

        if (valor == 20) {
            System.out.println("Sim é valor é " + valor);
        } else if (valor == 30) {
            System.out.println("Sim o valor é " + valor);
        } else if (valor == 1) {
            System.out.println("Sim o valor é " + valor);
        } else {
            System.out.println("Não o valor é " + valor);
        }

        System.out.println("Fim da Aplicação");

    }

}
